    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       /** fonction pour se connecter et verifier que le user et password sont correct */
           rs = db.querySelectAll("users", "username='" + txt_username.getText() + "' and password='" + txt_password.getText() + "'");
        try {
            while (rs.next()) {
                username1 = rs.getString("username");
                password1 = rs.getString("password");
                hak = rs.getString("type");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (username1 == null && password1 == null) {
            JOptionPane.showMessageDialog(this, "Le nom d'utilisateur ou le mot de pass est incorrect.");
        } else {
            if (hak.equals("directeur")) {
                Principale h = new Principale(); // si c'est bachir qui se connecte l'app va ouvrir le menu principale
                h.setVisible(true);
                this.dispose(); 
            } else {
                Caisse k = new Caisse(); // si c'est un caissier qui se connecte l'app va ouvrir le menu de la caisse
                k.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

